var cityService = require('../app/services/CityService');

exports.index = function(req, res) {
    var objects = [];
    cityService.getData(function(data) {
        objects = data;
        console.log(objects);
        res.render('object', {
            title: 'city',
            name: 'citys',
            dataName: '城市数据',
            saveRoute: 'city',
            exportRoute: 'citys',
            objects: JSON.stringify(objects)
        });
    });
}

exports.update = function(req, res) {
    var id = req.params.id;
    var object = req.body.object;
    object = JSON.parse(object);
    cityService.update(object, function(data) {
        var result = {
            result: 1
        };
        res.send(JSON.stringify(result));
    });
}

